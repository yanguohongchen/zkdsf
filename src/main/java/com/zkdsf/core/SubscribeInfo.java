package com.zkdsf.core;

public class SubscribeInfo {

	private String registerclienthostname;

	private String ip;

	private String servicename;

	public String getRegisterclienthostname() {
		return registerclienthostname;
	}

	public void setRegisterclienthostname(String registerclienthostname) {
		this.registerclienthostname = registerclienthostname;
	}

	public String getIp() {
		return ip;
	}

	public void setIp(String ip) {
		this.ip = ip;
	}

	public String getServicename() {
		return servicename;
	}

	public void setServicename(String servicename) {
		this.servicename = servicename;
	}

}
